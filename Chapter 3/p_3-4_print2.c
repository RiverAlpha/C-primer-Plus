//更多printf特性
#include<stdio.h>
int main(){
    unsigned int un = 3000000000;
    short end = 200;
    long big = 65537;
    long long verybig = 12345678908642;

    printf("un = %u and not %d\n",un,un); //Chapter 15
    /*
    un = 3000000000 and not -1294967296
    unsigned int 是无符号整数，范围是 0 到 4,294,967,295（对于32/64位系统）。
    int 是有符号整数，范围是 -2,147,483,648 到 2,147,483,647（对于32/64位系统）。
    3,000,000,000 超过了 int 的最大值 2,147,483,647。
    3,000,000,000 的二进制是 10110010 10101010 00001110 10000000
    当解释为%d（有符号类型时） 最高位为1表示负数

    表示方式	二进制（32位）	十六进制	十进制值
    ​无符号	10110010 10101010 00001110 10000000	0xB2AA0E80	3,000,000,000
    ​有符号（溢出）​	10110010 10101010 00001110 10000000	0xB2AA0E80	-1,294,967,296

    计算 3,000,000,000 在 32 位有符号整数中的实际表示：

    32 位有符号整数的模为 2³² = 4,294,967,296。
    溢出的值：3,000,000,000 - 4,294,967,296 = -1,294,967,296。
    因此，3,000,000,000 会被解释为 -1,294,967,296。 
    */
    printf("end = %hd and %d\n",end,end);
    printf("big = %ld and not %hd\n",big,big);
    printf("verybig = %lld and not %ld\n",verybig,verybig);
    return 0;

    /*
    打印unsigned int类型的值 使用%u转换说明
    打印long类型的值，使用%ld转换说明
    如果系统中int和long的大小相同使用 %d就行
    在x和o前面可以使用l前缀，%lx表示以十六进制格式打印long类型整数
    %lo表示八进制显示long类型的整数
    %hd表示以十进制显示short类型的整数，%ho表示以八进制显示short类型的整数
    h和l前缀都可以和u一起使用，用于表示无符号类型 例如%lu表示打印unsigned long类型的值
    对于支持long long类型的系统，%lld和%llu分别表示有符号和无符号类型
    */
}